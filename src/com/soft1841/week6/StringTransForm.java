package com.soft1841.week6;

/**
 * 大小写转换
 */
public class StringTransForm {
    public static void main(String[] args) {
        //创建字符串
        String str = "abc DEF";
        //按小写格式输出
        System.out.println(str.toLowerCase());
        //按大写格式输出
        System.out.println(str.toUpperCase());
    }
}
